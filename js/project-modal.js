let lastFocus = null;

function getModalElements() {
  return {
    modal: document.getElementById('projectModal'),
    title: document.getElementById('projectModalTitle'),
    subtitle: document.getElementById('projectModalSubtitle'),
    sidebar: document.getElementById('projectModalSidebar'),
    gallery: document.getElementById('projectModalGallery'),
    closeBtn: document.querySelector('.project-modal__close'),
  };
}

function renderGallery(images) {
  return images
    .map(
      ({ src, alt, layout }) => `
        <figure class="project-modal__gallery-item project-modal__gallery-item--${layout}">
          <img src="${src}" alt="${alt}" loading="lazy">
        </figure>
      `
    )
    .join('');
}

function openProjectModal(projectId) {
  const data = PROJECT_DETAILS[projectId];
  const { modal, title, subtitle, sidebar, gallery, closeBtn } = getModalElements();
  if (!data || !modal) return;

  const trustModal = document.getElementById('trustModal');
  if (trustModal?.classList.contains('is-open')) {
    trustModal.classList.remove('is-open');
    trustModal.setAttribute('aria-hidden', 'true');
  }

  title.textContent = data.title;
  subtitle.textContent = data.subtitle;
  sidebar.textContent = data.sidebar;
  gallery.innerHTML = renderGallery(data.images);

  lastFocus = document.activeElement;
  modal.classList.add('is-open');
  modal.setAttribute('aria-hidden', 'false');
  document.body.classList.add('modal-open');
  closeBtn?.focus();
}

function closeProjectModal() {
  const { modal } = getModalElements();
  if (!modal) return;

  modal.classList.remove('is-open');
  modal.setAttribute('aria-hidden', 'true');
  document.body.classList.remove('modal-open');

  if (lastFocus && typeof lastFocus.focus === 'function') {
    lastFocus.focus();
  }
}

function initProjectModal() {
  const { modal } = getModalElements();
  if (!modal) return;

  document.querySelectorAll('[data-project]').forEach((trigger) => {
    trigger.addEventListener('click', (event) => {
      event.preventDefault();
      const id = trigger.getAttribute('data-project');
      if (id) openProjectModal(id);
    });

    if (trigger.getAttribute('role') === 'button') {
      trigger.addEventListener('keydown', (event) => {
        if (event.key === 'Enter' || event.key === ' ') {
          event.preventDefault();
          const id = trigger.getAttribute('data-project');
          if (id) openProjectModal(id);
        }
      });
    }
  });

  modal.querySelectorAll('[data-close-modal]').forEach((el) => {
    el.addEventListener('click', closeProjectModal);
  });

  document.addEventListener('keydown', (event) => {
    if (event.key === 'Escape' && modal.classList.contains('is-open')) {
      closeProjectModal();
    }
  });
}

document.addEventListener('sectionsLoaded', initProjectModal);
